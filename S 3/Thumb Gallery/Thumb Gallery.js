function changeImagesSupra() {
  const supra = document.querySelector("#supra-car");
  const text = document.querySelector("h3");
  const supraC = document.querySelector(".supra");
  supra.setAttribute("src", "supra.jpg");
  text.textContent = "Toyota Supra";
  supraC.style.border = "2px solid red";
}

